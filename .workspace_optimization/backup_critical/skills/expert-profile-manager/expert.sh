# 添加咨询记录
log_consultation() {
    local expert="$1"
    local date="$2"
    local topic="$3"
    
    init_dirs
    local logfile="${LOGS_DIR}/${date}-${expert}.md"
    
    cat >> "$logfile" << EOF
# 咨询记录 - ${expert} - ${date}

**主题:** ${topic}

**形式:** (线上/线下)

**核心讨论内容:**
（待填写...）

**专家建议:**
（待填写...）

**后续行动项:**
- [ ] 

**记录时间:** $(date '+%Y-%m-%d %H:%M:%S')
EOF

    echo "✅ 咨询记录已创建: $logfile"
}


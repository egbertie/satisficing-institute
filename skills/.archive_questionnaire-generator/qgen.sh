- **80-100分**: 强烈推荐，建议进入深度尽调
- **60-79分**: 可以考虑，建议补充压力测试
- **40-59分**: 存在明显顾虑，建议谨慎观察
- **40分以下**: 不推荐，建议婉拒

**感知力特别关注:**
如果「直觉层」得分显著低于其他维度（差距>10分），
建议暂停理性分析，深入探索直觉预警的原因。

---

## 📝 评估人信息

| 项目 | 内容 |
|------|------|

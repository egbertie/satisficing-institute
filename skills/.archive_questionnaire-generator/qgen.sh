#!/bin/bash
# questionnaire-generator.sh - 合伙人评估问卷生成器
# 满意解研究所专用

OUTPUT_DIR="${HOME}/.openclaw/questionnaires"
TEMPLATE_DIR="$(dirname "$0")/templates"

# 初始化
init() {
    mkdir -p "$OUTPUT_DIR"
}

# 生成问卷头部
generate_header() {
    local type="$1"
    local date=$(date '+%Y-%m-%d')
    
    cat << EOF
---
title: 合伙人评估问卷 - ${type}
date: ${date}
generated_by: 满意解研究所问卷生成器 v1.0
methodology: 五路图腾感知力决策
---

# 合伙人评估问卷

**问卷类型:** ${type}  
**生成日期:** ${date}  
**评估方法论:** 五路图腾感知力决策  
**出品:** 满意解研究所

---

## 📋 填写说明

本问卷基于「左脑逻辑 + 右脑直觉」的综合决策方法论设计。

**评估维度:**
- 🧠 认知层 (30%)：经验、能力、资源匹配度
- ❤️ 情感层 (30%)：价值观、信任度、默契度  
- 🎯 行为层 (20%)：行动习惯、承诺兑现
- 🔮 直觉层 (20%)：感知力判断、气场匹配

**填写建议:**
1. 独立完成，避免相互干扰
2. 认知/行为层请客观打分
3. 直觉层请放松后凭第一感觉回答
4. 整个问卷约需 ${TIME_ESTIMATE:-"15-30"} 分钟

---

## 👤 基本信息

| 项目 | 填写内容 |
|------|----------|
| 姓名 | _________________ |
| 当前职位 | _________________ |
| 从业年限 | _________________ |
| 推荐来源 | _________________ |
| 评估日期 | _________________ |

---

EOF
}

# 生成认知层题目

# 生成直觉层题目
generate_intuition() {
    cat << 'EOF'
## 🔮 第四部分：直觉层评估 (20%)

### 4.1 感知力判断

**Q11:** 闭上眼睛，想象与候选人并肩工作的场景：
- [ ] 画面清晰，感到兴奋 (10分)
- [ ] 画面较清晰，感到期待 (7分)

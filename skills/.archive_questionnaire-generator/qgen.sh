}

# 生成初筛问卷
generate_screening() {
    init
    local output="${OUTPUT_DIR}/questionnaire-screening-$(date +%Y%m%d).md"
    TIME_ESTIMATE="5-10"
    
    generate_header "初筛版" > "$output"
    generate_cognitive | head -20 >> "$output"
    generate_emotional | head -20 >> "$output"
    cat << 'EOF' >> "$output"
## 📊 快速评估

| 维度 | 关键问题 | 得分 |
|------|----------|------|
| 经验匹配 | Q1+Q2 | ___/20 |
| 价值观 | Q5 | ___/10 |
| 信任度 | Q7 | ___/10 |
| 直觉 | Q13 | ___/10 |
| **合计** | | **___/50** |

**初筛标准:**
- ≥35分: 通过初筛，进入深度评估
- <35分: 暂不考虑

EOF
    
    echo "✅ 初筛问卷已生成: $output"
}

# 生成深度问卷
generate_deep() {
    init
    local output="${OUTPUT_DIR}/questionnaire-deep-$(date +%Y%m%d).md"
    TIME_ESTIMATE="25-35"
    
    generate_header "深度评估版" > "$output"
    generate_cognitive >> "$output"
    generate_emotional >> "$output"
    generate_behavior >> "$output"
    generate_intuition >> "$output"
    generate_summary >> "$output"
    
    echo "✅ 深度评估问卷已生成: $output"
}

# 生成压力测试问卷
generate_stress() {
    init
    local output="${OUTPUT_DIR}/questionnaire-stress-$(date +%Y%m%d).md"
    
    cat > "$output" << 'EOF'
---
title: 72小时压力测试 - 合伙人实战评估
methodology: 极限72小时
---

# 《极限72小时》合伙人压力测试

**测试周期:** 72小时连续挑战  
**评估方式:** 实战观察 + 事后复盘  
**通过标准:** 完成全部任务 + 综合评分≥70分

---

## 测试任务清单

### 第一天：决策压力 (24h)

- [ ] **任务1**: 给定一个真实业务难题，24小时内给出决策方案
- [ ] **观察点**: 决策速度、信息收集能力、决断力

### 第二天：协作压力 (24h)

- [ ] **任务2**: 与创始人共同完成一个紧急项目交付
- [ ] **观察点**: 沟通效率、分工配合、冲突处理

### 第三天：突发压力 (24h)

- [ ] **任务3**: 故意设置一个突发事件，观察应变能力
- [ ] **观察点**: 情绪稳定性、危机处理、责任担当

---

## 评估量表

| 维度 | Day1 | Day2 | Day3 | 平均 |
|------|------|------|------|------|
| 专业能力 | /10 | /10 | /10 | /10 |
| 协作配合 | /10 | /10 | /10 | /10 |
| 情绪稳定 | /10 | /10 | /10 | /10 |
| 责任担当 | /10 | /10 | /10 | /10 |
| **总分** | | | | **/40** |

**通过标准: ≥28分**

EOF
    
    echo "✅ 72小时压力测试问卷已生成: $output"
}

# 主命令处理
case "$1" in
    "screening"|"初筛")
        generate_screening
        ;;
    "deep"|"深度")
        generate_deep
        ;;
    "stress"|"压力测试")
        generate_stress
        ;;
    *)
        echo "合伙人评估问卷生成器 - 满意解研究所"
        echo ""
        echo "用法:"
        echo "  ./qgen.sh screening    # 生成初筛问卷 (5分钟)"
        echo "  ./qgen.sh deep         # 生成深度评估问卷 (30分钟)"
        echo "  ./qgen.sh stress       # 生成72小时压力测试"
        echo ""
        echo "输出目录: $OUTPUT_DIR"
        ;;
esac
